#define inf INT_MAX
class Solution
{
public:
    //Function to find the shortest distance of all the vertices
    //from the source vertex S.
    vector<int> dijkstra(int V, vector<vector<int>> adj[], int S)
    {
        vector<int> minDist(V, inf);
        minDist[S] = 0;

        vector<bool> vis(V, false);

        priority_queue<pair<int, int>> minHeap;
        minHeap.push({0, S});

        while (!minHeap.empty())
        {
            int curr = minHeap.top().second;
            minHeap.pop();
            // as heap may have duplicate pair
            if(vis[curr])
                continue;
            vis[curr] = true;

            //cout << "curr: " << curr << endl;

            for (auto i : adj[curr])
            {
                int u = curr;
                int v = i[0];
                int wt = i[1];
                if (vis[v])
                    continue;
                int newDist = minDist[u] + wt;
                if (minDist[v] > newDist)
                    minDist[v] = newDist;

                minHeap.push({-minDist[v], v});
            }
        }
        return minDist;
    }
};

